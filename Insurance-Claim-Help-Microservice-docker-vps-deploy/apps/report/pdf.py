def _damage_body(payload, styles):
    if not payload["damages"]:
        return Paragraph("No damages were found for this room.", styles["BandBody"])

    rows = [["Type", "Location", "Severity"]]
    for damage in payload["damages"]:
        rows.append(
            [
                str(damage.get("type", "Unspecified")),
                str(damage.get("location", "Unknown")),
                str(damage.get("severity", "unknown")).title(),
            ]
        )
    return _table_on_dark(rows, [2.1 * inch, 2.1 * inch, 0.85 * inch])


def _repair_body(payload, styles):
    if not payload["repair_items"]:
        return Paragraph("No repair items are currently associated with this room.", styles["BandBody"])

    rows = [["ID", "Material", "Quantity", "Cost"]]
    for item in payload["repair_items"]:
        rows.append(
            [
                str(item["id"]),
                str(item["material"]),
                str(item["quantity"]),
                _currency(item["cost"]),
            ]
        )
    return _table_on_dark(rows, [0.55 * inch, 2.75 * inch, 0.85 * inch, 0.9 * inch])


def _media_body(payload, styles):
    blocks = []
    for item in payload["media"]:
        parts = [
            Paragraph(f"Media #{item['id']}", styles["MediaTitle"]),
            Spacer(1, 6),
        ]
        if item.get("image_path"):
            parts.append(
                _framed_image(
                    item["image_path"],
                    max_width=4.55 * inch,
                    max_height=3.5 * inch,
                )
            )
        else:
            parts.append(Paragraph("Image preview unavailable.", styles["BandBody"]))
        card = Table([[parts]], colWidths=[4.65 * inch])
        card.setStyle(
            TableStyle(
                [
                    ("BACKGROUND", (0, 0), (-1, -1), WHITE),
                    ("BOX", (0, 0), (-1, -1), 0, colors.transparent),
                    ("LEFTPADDING", (0, 0), (-1, -1), 14),
                    ("RIGHTPADDING", (0, 0), (-1, -1), 14),
                    ("TOPPADDING", (0, 0), (-1, -1), 14),
                    ("BOTTOMPADDING", (0, 0), (-1, -1), 14),
                ]
            )
        )
        blocks.append(card)
        blocks.append(Spacer(1, 10))
    return KeepTogether(blocks[:-1] if blocks else [Paragraph("No media files are attached to this room.", styles["BandBody"])])


def _media_section(payload, styles):
    blocks = [
        Paragraph("MEDIA", styles["MediaSectionTitle"]),
        Spacer(1, 8),
    ]
    for index, item in enumerate(payload["media"], start=1):
        blocks.append(_media_card(item, styles))
        if index < len(payload["media"]):
            blocks.append(Spacer(1, 8))
    return blocks


