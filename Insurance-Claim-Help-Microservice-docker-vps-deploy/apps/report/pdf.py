def _metric(label: str, value: str, styles):
    return Paragraph(
        f'<font color="#7B8CA4"><b>{label.upper()}</b></font><br/><font color="#16324F"><b>{value}</b></font>',
        styles["Metric"],
    )


def _detail_card(title: str, rows: list[tuple[str, str]], styles):
    body = [Paragraph(title, styles["SectionCardTitle"]), Spacer(1, 8)]
    for label, value in rows:
        body.append(
            Paragraph(
                f'<font color="#7B8CA4"><b>{label.upper()}</b></font><br/><font color="#16324F">{_escape(value)}</font>',
                styles["CardBody"],
            )
        )
        body.append(Spacer(1, 6))

    table = Table([[body[:-1]]], colWidths=[3.4 * inch])
    table.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (-1, -1), WHITE),
                ("BOX", (0, 0), (-1, -1), 1, BORDER),
                ("LEFTPADDING", (0, 0), (-1, -1), 16),
                ("RIGHTPADDING", (0, 0), (-1, -1), 16),
                ("TOPPADDING", (0, 0), (-1, -1), 14),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 14),
                ("VALIGN", (0, 0), (-1, -1), "TOP"),
            ]
        )
    )
    return table


def _band_title_text(title: str) -> str:
    mapping = {
        "Damage Assessment": "DAMAGE<br/>ASSESSMENT",
        "Repair Items": "REPAIR<br/>ITEMS",
    }
    return mapping.get(title, title.upper())


def _framed_image(path: str, max_width: float, max_height: float):
    image = _scaled_image(path, max_width=max_width, max_height=max_height)
    table = Table([[image]], colWidths=[max_width + 0.2 * inch])
    table.setStyle(
        TableStyle(
            [
                ("BACKGROUND", (0, 0), (-1, -1), WHITE),
                ("BOX", (0, 0), (-1, -1), 1.2, BORDER),
                ("LEFTPADDING", (0, 0), (-1, -1), 8),
                ("RIGHTPADDING", (0, 0), (-1, -1), 8),
                ("TOPPADDING", (0, 0), (-1, -1), 8),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 8),
                ("ALIGN", (0, 0), (-1, -1), "CENTER"),
            ]
        )
    )
    return table


def _scaled_image(path: str, max_width: float, max_height: float):
    with PILImage.open(path) as img:
        width, height = img.size
    scale = min(max_width / width, max_height / height)
    return Image(path, width=width * scale, height=height * scale)


def _currency(value) -> str:
    try:
        return f"£{float(value):,.2f}"
    except (TypeError, ValueError):
        return str(value)


def _ownership_status(property_data: dict) -> str:
    if not any(
        key in property_data
        for key in ["ownership_verified", "ownership_rejected", "ownership_reject_reason"]
    ):
        return "Not available"
    if property_data.get("ownership_verified"):
        return "Verified"
    if property_data.get("ownership_rejected"):
        reason = property_data.get("ownership_reject_reason")
        return f"Rejected ({reason})" if reason else "Rejected"
    return "Pending"


def _safe_value(value) -> str:
    if value in (None, ""):
        return "Not available"
    return str(value)


def _escape(value) -> str:
    return (
        _safe_value(value)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
    )


