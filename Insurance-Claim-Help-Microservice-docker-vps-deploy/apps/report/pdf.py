def _context_grid(payload: dict, styles):
    property_data = payload.get("property") or {}
    user_data = payload.get("user") or {}

    owner_name = (
        user_data.get("name")
        or user_data.get("full_name")
        or " ".join(
            part
            for part in [
                user_data.get("first_name"),
                user_data.get("middle_name"),
                user_data.get("last_name"),
            ]
            if part
        )
        or "Not available"
    )
    address = (
        property_data.get("address")
        or property_data.get("address_line")
        or user_data.get("address_line_1")
        or "Not available"
    )
    owner_role = user_data.get("role_name") or user_data.get("role") or "Not available"
    property_type = (
        property_data.get("property_type") or user_data.get("property_type") or "Not available"
    )
    postcode = property_data.get("postcode") or user_data.get("postcode") or "Not available"
    contact_number = user_data.get("contact_number") or "Not available"
    email = user_data.get("email") or "Not available"

    data = [
        [
            _detail_card(
                "Property Details",
                [
                    ("Property ID", _safe_value(payload.get("property_id"))),
                    ("Address", address),
                    ("Type", property_type),
                    ("Postcode", postcode),
                ],
                styles,
            ),
            _detail_card(
                "Homeowner Details",
                [
                    ("Name", owner_name),
                    ("Role", owner_role),
                    ("Email", email),
                    ("Contact", contact_number),
                ],
                styles,
            ),
        ]
    ]
    table = Table(data, colWidths=[3.525 * inch, 3.525 * inch])
    table.setStyle(
        TableStyle(
            [
                ("BOX", (0, 0), (-1, -1), 0, colors.transparent),
                ("VALIGN", (0, 0), (-1, -1), "TOP"),
                ("LEFTPADDING", (0, 0), (-1, -1), 0),
                ("RIGHTPADDING", (0, 0), (-1, -1), 0),
                ("TOPPADDING", (0, 0), (-1, -1), 0),
                ("BOTTOMPADDING", (0, 0), (-1, -1), 0),
            ]
        )
    )
    return table


